    private void jButton1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MousePressed
        // TODO add your handling code here:
        // vars
        String crsName = "";
        boolean isThere = false;
        boolean isGood = false;
        boolean isFree = false;
        int crsTime = 0;
        Room room = null;
        
        crsName = addCrsName.getText();
        
        for (Course crs : crs) {
            if (crsName == crs.getCName()) {
                isThere = true;
                break;
            }
            else {
                isGood = true;
            }
        }
        switch (addCrsTime.getSelectedIndex()) {
            case 0:
                JOptionPane.showMessageDialog(this, "Need to select a time");
                break;
            case 1:
                crsTime = 9;
                break;
            case 2:
                crsTime = 10;
                break;
            case 3:
                crsTime = 11;
                break;
            case 4:
                crsTime = 12;
                break;
            case 5:
                crsTime = 1;
                break;
            case 6:
                crsTime = 2;
                break;
            case 7:
                crsTime = 3;
                break;
            case 8:
                crsTime = 4;
                break;
            case 9:
                crsTime = 5;
                break;
        }
        for (Room r : rm) {
            if (!r.getCourse().isEmpty()) {
                ArrayList<Course> c = r.getCourse();
                for (Course crs : c) {
                    if (crs.getCtm() == crsTime) {
                        JOptionPane.showMessageDialog(this, "Time is already taken");
                        isGood = false;
                    }
                    else {
                        isGood = true;
                    }
                }
            }
        }
        
        Room roomSelect = (Room) addCrsRmList.getSelectedItem();
        for (Room i : rm) {
            if (isFree == true) {
                if (addCrsRmList.getSelectedIndex() != 0) {
                    room = rm.get(addCrsRmList.getSelectedIndex() - 1);
                    isGood = true;
                }
                else {
                    JOptionPane.showMessageDialog(this, "Need to select a room");
                    isGood = false;
                }
            }
        }
        
        // checking if student added to course
        if (studInCrs.isEmpty()) {
            
        } else {
            Course course = new Course(crsName, crsTime, room, studInCrs); 
            for (Student i : studInCrs) {
                for (Student j : stud) {
                    if (i == j) {
                        j.addCourse(course);
                    }
                }
                i.addCourse(course);
            }
            crs.add(course);
            room.addCourse(course);
        }

        if (isThere == true) {
            JOptionPane.showMessageDialog(this, "Course Already Exits");
        }
        
        if (isGood == true) {
            // updating the course and room and resetting everything
            Course course = new Course(crsName, crsTime, room);
            crs.add(course);
            room.addCourse(course);
            addCrsName.setText("");
            addCrsTime.setSelectedIndex(0);
            addCrsRmList.setSelectedIndex(0);
            addCrsStuList.setSelectedIndex(0);
            

        }
        else {
            JOptionPane.showMessageDialog(this, "Didn't set something correctly");
        }
        
        for (Course c : crs) {
            addCrsList.append("Course Name: " + c.getCName() + "\tCourse Time: "
            + c.getCtm() + "\tRoom: " + c.getCrm().getNum() + "\n");
        }
        addCrsList.repaint();
    }//GEN-LAST:event_jButton1MousePressed

