    private void addStuBtnMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addStuBtnMousePressed
        // TODO add your handling code here:
        // vars for inputting
        String firstName = "";
        String lastName = "";
        String phoneNum = "";
        boolean isGood = false;
        // setting the text field to the vars
        firstName = addStuFirst.getText();
        lastName = addStuLast.getText();
        phoneNum = addStuPhone.getText();
        
        // if phone num is not ten display prompt
        if (phoneNum.length() != 10) {
            JOptionPane.showMessageDialog(this, "Not enough digits");
            isGood = false;
        }
        
        // if first and last name exist set isThere to true
        boolean isThere = false;
        if (!stud.isEmpty()) {
            for (Student stu : stud) {
                if (firstName == stu.getFirst()) {
                    if (lastName == stu.getLast()) {
                        if (phoneNum == stu.getPhone()) {
                            isThere = true;
                            break;
                        }
                    }
                }
            }
        }
        
        /* if there show message dialog, if not create new student and reset
        field
        */
        if (isThere == true) {
            JOptionPane.showMessageDialog(this, "Student Already exists");
        }
        else {
            stud.add(new Student(firstName, lastName, phoneNum));
            addStuFirst.setText("");
            addStuLast.setText("");
            addStuPhone.setText("");
        }
        
        // reset field
        addStuFirst.setText("");
        addStuLast.setText("");
        addStuPhone.setText("");
        addStuList.setText("");
        
        // add to jlist
        for (Student stu : stud) {
            addStuList.append("First Name: " + stu.getFirst() + "\tLast Name: " 
            + stu.getLast() + "\tPhone Number: " + stu.getPhone() + "\n");
        }
        addStuList.repaint();
    }//GEN-LAST:event_addStuBtnMousePressed

