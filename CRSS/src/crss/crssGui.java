    private void addCrsBtnMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addCrsBtnMousePressed
        // TODO add your handling code here:
        // vars
        String crsName = "";
        boolean isThere = false;
        boolean isGood = true;
        boolean isFree = false;
        int crsTime = 0;
        Room room = null;
        
        crsName = addCrsName.getText();
        
        if (crsName.length() == 0) {
            JOptionPane.showMessageDialog(this, "Course needs a name");
            isGood = false;
        }
        
        for (Course crs : crs) {
            if (crsName == crs.getCName()) {
                isThere = true;
                break;
            }
            else {
                isGood = false;
            }
        }
        
                
        if (addCrsRmList.getSelectedIndex() != 0) {
            room = rm.get(addCrsRmList.getSelectedIndex() - 1);
            isGood = true;
        }
        else {
            JOptionPane.showMessageDialog(this, "Need to select a room");
            isGood = false;
        }
        
        switch (addCrsTime.getSelectedIndex()) {
            case 0:
                JOptionPane.showMessageDialog(this, "Need to select a time");
                break;
            case 1:
                crsTime = 9;
                break;
            case 2:
                crsTime = 10;
                break;
            case 3:
                crsTime = 11;
                break;
            case 4:
                crsTime = 12;
                break;
            case 5:
                crsTime = 1;
                break;
            case 6:
                crsTime = 2;
                break;
            case 7:
                crsTime = 3;
                break;
            case 8:
                crsTime = 4;
                break;
            case 9:
                crsTime = 5;
                break;
            default:
                crsTime = 9;
                break;
        }
        if (!room.empty()) {
            ArrayList<Course> c = room.getCourse();
            
            for (Course i : c) {
                if (i.getCtm() == crsTime) {
                    JOptionPane.showMessageDialog(this, "Room time conflict");
                    isGood = false;
                    return;
                }
                else {
                    isGood = true;
                }
            }
        }

   
        
        // checking if student added to course
        if (studInCrs.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please add a student");
        } else {
            Course course = new Course(crsName, crsTime, room, studInCrs); 
            for (Student i : studInCrs) {
                for (Student j : stud) {
                    if (i == j) {
                        j.addCourse(course);
                    }
                }
                //i.addCourse(course);
            }
            crs.add(course);
            room.addCourse(course);
            addCrsName.setText("");
            addCrsTime.setSelectedIndex(0);
            addCrsRmList.setSelectedIndex(0);
            addCrsStuList.setSelectedIndex(0);
            isGood = false;
        }

        if (isThere == true) {
            JOptionPane.showMessageDialog(this, "Course Already Exits");
        }
        
        if (isGood == true) {
            // updating the course and room and resetting everything
            Course course = new Course(crsName, crsTime, room);
            crs.add(course);
            room.addCourse(course);
            addCrsName.setText("");
            addCrsTime.setSelectedIndex(0);
            addCrsRmList.setSelectedIndex(0);
            addCrsStuList.setSelectedIndex(0);
        }

        addCrsList.setText("");
        for (Course c : crs) {
            String extra = ":00pm";
            if (c.getCtm() > 8 && c.getCtm() < 12) {
                extra = ":00am";
            }
            addCrsList.append("Course Name: " + c.getCName() + "\tCourse Time: "
            + c.getCtm() + extra + "\tRoom: " + c.getCrm().getNum() + "\n");
        }
        addCrsList.repaint();
    }//GEN-LAST:event_addCrsBtnMousePressed

